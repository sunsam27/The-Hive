export function formatCurrency(amount, currencyCode = 'USD') {
  const currency = CURRENCIES.find((c) => c.code === currencyCode) || CURRENCIES[0];
  const formatted = (Number(amount) || 0).toFixed(2);
  return `${currency.symbol}${formatted}`;
}
